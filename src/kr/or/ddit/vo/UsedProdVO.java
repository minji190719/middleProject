package kr.or.ddit.vo;

public class UsedProdVO {
	private String u_prod_no;
	private String req_no;
	private String u_prod_quality;
	private int u_prod_price;
	private String u_prod_date;
	private String u_prod_status;
	
	public String getU_prod_no() {
		return u_prod_no;
	}
	public void setU_prod_no(String u_prod_no) {
		this.u_prod_no = u_prod_no;
	}
	public String getReq_no() {
		return req_no;
	}
	public void setReq_no(String req_no) {
		this.req_no = req_no;
	}
	public String getU_prod_quality() {
		return u_prod_quality;
	}
	public void setU_prod_quality(String u_prod_quality) {
		this.u_prod_quality = u_prod_quality;
	}
	public int getU_prod_price() {
		return u_prod_price;
	}
	public void setU_prod_price(int u_prod_price) {
		this.u_prod_price = u_prod_price;
	}
	public String getU_prod_date() {
		return u_prod_date;
	}
	public void setU_prod_date(String u_prod_date) {
		this.u_prod_date = u_prod_date;
	}
	public String getU_prod_status() {
		return u_prod_status;
	}
	public void setU_prod_status(String u_prod_status) {
		this.u_prod_status = u_prod_status;
	}
}
