package kr.or.ddit.vo;

public class UsedCartVO {
	private String u_cart_no;
	private String pay_no;
	private String u_prod_no;
	private String u_cart_status;
	private String u_cart_date;
	
	public String getU_cart_no() {
		return u_cart_no;
	}
	public void setU_cart_no(String u_cart_no) {
		this.u_cart_no = u_cart_no;
	}
	public String getPay_no() {
		return pay_no;
	}
	public void setPay_no(String pay_no) {
		this.pay_no = pay_no;
	}
	public String getU_prod_no() {
		return u_prod_no;
	}
	public void setU_prod_no(String u_prod_no) {
		this.u_prod_no = u_prod_no;
	}
	public String getU_cart_status() {
		return u_cart_status;
	}
	public void setU_cart_status(String u_cart_status) {
		this.u_cart_status = u_cart_status;
	}
	public String getU_cart_date() {
		return u_cart_date;
	}
	public void setU_cart_date(String u_cart_date) {
		this.u_cart_date = u_cart_date;
	}
}
